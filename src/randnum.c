#define  B1  (-5.447609879822406e+01)
#define  B2   1.615858368580409e+02
#define  B3  (-1.556989798598866e+02)
#define  B4   6.680131188771972e+01
#define  B5  (-1.328068155288572e+01)

#define  C1  (-7.784894002430293e-03)
#define  C2  (-3.223964580411365e-01)
#define  C3  (-2.400758277161838e+00)
#define  C4  (-2.549732539343734e+00)
#define  C5   4.374664141464968e+00
#define  C6   2.938163982698783e+00

#define  D1   7.784695709041462e-03
#define  D2   3.224671290700398e-01
#define  D3   2.445134137142996e+00
#define  D4   3.754408661907416e+00

#define P_LOW   0.02425
/* P_high = 1 - p_low*/
#define P_HIGH  0.97575

/* inverse normal cumulative distribution function */
double Prose_normsinv(double p)
{
    double x;
    double q, r, u, e;

    /* three cases, lower region, central region and upper region */
    /* the idea is here: https://web.archive.org/web/20151030215612/http://home.online.no/~pjacklam/notes/invnorm/#C */
    if ((0 < p )  && (p < P_LOW)){
        q = sqrt(-2*log(p));
        x = (((((C1*q+C2)*q+C3)*q+C4)*q+C5)*q+C6) / ((((D1*q+D2)*q+D3)*q+D4)*q+1);
    }
    else {
        if ((P_LOW <= p) && (p <= P_HIGH)){
           q = p - 0.5;
           r = q*q;
           x = (((((A1*r+A2)*r+A3)*r+A4)*r+A5)*r+A6)*q /(((((B1*r+B2)*r+B3)*r+B4)*r+B5)*r+1);
        }
        else{
                if ((P_HIGH < p)&&(p < 1)){
                   q = sqrt(-2*log(1-p));
                   x = -(((((C1*q+C2)*q+C3)*q+C4)*q+C5)*q+C6) / ((((D1*q+D2)*q+D3)*q+D4)*q+1);
                }
        }
}

/* If you are compiling this under UNIX OR LINUX, you may uncomment this block for better accuracy.\> */
if(( 0 < p)&&(p < 1)){
   e = 0.5 * erfc(-x/sqrt(2)) - p;
   u = e * sqrt(2*M_PI) * exp(x*x/2);
   x = x - u/(1 + x*u/2);
}

return x;
}

double Prose_generate_random_param(double low, double up)
{

   struct timeval tv;
   double u;

   gettimeofday(&tv,0);
   unsigned long myseed = tv.tv_sec + tv.tv_usec;
   srand(myseed);
   u = (double)rand() / (double)RAND_MAX;

   return (low + u*(up - low));
}

double Prose_generate_random_norm_param(double sigma)
{
  
  double prob, u;

  /* generate a random value prob uniformly distributed on [0,1]*/
  /*  this u can be treated as a cumulative probability of u ~ N(0,1) */
  prob = Prose_generate_random_param(0.,1.);
  
  /* to avoid 0. and 1. */
  if(prob == 0.)
      prob = EPS_TS;
  if(prob == 1.)
      prob = 1. - EPS_TS;

  /* computing the inverse normal cumulative distribution function */
  u = Prose_normsinv(prob); // u ~ N(0,1)

  /* transform u to u ~ N(0, sigma^2); */
  u *= sigma;

  return u;
}

